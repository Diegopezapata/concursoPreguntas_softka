    public void llenarPreguntas ()
    {
        
        PregunConcurso pregunta = juego.seleccionarPreguntaParaRonda(ronda);
            labelPregunta.setText(pregunta.getDescripPregunta());
            String categoria = juego.buscarCategoriaPorRonda(ronda);
            
            labelCategoria.setText(categoria);
            txtRonda.setText(String.valueOf(ronda));
            sumarAcumulado(ronda);
            txtPremio.setText(String.valueOf(acumulado));
            
            List<OpcionesPreguntas> listaOpciones = juego.listaOpcionesParaPregunta(pregunta);
            
            for (int i = 0 ; i< listaOpciones.size();i++)
            {
                if(i == 0) 
                {
                    opcion1.setText(listaOpciones.get(i).getDescripcionOpcion());
                }
                if(i == 1) 
                {
                    opcion2.setText(listaOpciones.get(i).getDescripcionOpcion());
                }
                if(i == 2) 
                {
                    opcion3.setText(listaOpciones.get(i).getDescripcionOpcion());
                }
                 if(i == 3) 
                {
                    opcion4.setText(listaOpciones.get(i).getDescripcionOpcion());
                }
            }
    }
    
  
