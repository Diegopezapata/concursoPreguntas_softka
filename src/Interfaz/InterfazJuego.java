    public void sumarAcumulado (int rondaA) {
        
        
       if (rondaA == 1) 
       {
           acumulado = acumulado + 100000;
       }
        if (rondaA == 2) 
       {
           acumulado = acumulado + 500000;
       }
         if (rondaA == 3) 
       {
           acumulado = acumulado + 1000000;
       }
          if (rondaA == 4) 
       {
           acumulado = acumulado + 1500000;
       }
           if (rondaA == 5) 
       {
           acumulado = acumulado + 5000000;
       }
               
       
    }
